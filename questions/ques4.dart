//Use switch-case to print the day name based on a number (1-7)

void main() {
  int days = 2;
  switch (days) {
    case 1:
      print("Sunday");
    case 2:
      print("Monday");
    case 3:
      print("Tuesday");
    case 4:
      print("Wednesday");
    case 5:
      print("Thusday");
    case 6:
      print("Friday");
    case 7:
      print("Saturday");
  }
}